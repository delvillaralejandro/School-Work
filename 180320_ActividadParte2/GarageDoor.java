
public class GarageDoor {
	
	public void Open() {
		System.out.println("Garage Door is opening...");
	}
	
	public void Close() {
		System.out.println("Garage Door is closing...");
	}
	
	public void turnLights() {
		System.out.println("Garage lights are on...");
	}
	
	public void turnOffLights() {
		System.out.println("Garage lights are off...");
	}
	
	public void openSideways() {
		System.out.println("The Gargage Door will open to the sides...");
	}
	
	public void openVertical() {
		System.out.println("The Garage Door will open vertically...");
	}
}
