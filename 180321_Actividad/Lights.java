
public class Lights {
	
	public void On() {
		System.out.println("Lights are on...");
	}
	
	public void Off() {
		System.out.println("Lights are off...");
	}
	
	public void whiteOn() {
		System.out.println("Lights are now White...");
	}
	
	public void whiteOff() {
		System.out.println("Lights are not White anymore...");
	}
	
	public void wWhiteOn() {
		System.out.println("Lights are now Warm White...");
	}

	public void wWhiteOff() {
		System.out.println("Lights are not Warm White anymore...");
	}
}
